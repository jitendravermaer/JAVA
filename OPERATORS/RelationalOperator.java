
public class RelationalOperator {

    public static void main(String[] args) {
        int p = 10;
        int q = 15;

        System.out.println(p == q);  //F
        System.out.println(p != q);  //T
        System.out.println(p <= q);  //T
        System.out.println(p >= q);  //F
        System.out.println(p < q);   //T
        System.out.println(p > q);   //F

    }
}
